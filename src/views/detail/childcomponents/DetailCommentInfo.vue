<template>
  <div>
    <div v-if="Object.keys(commentInfo).length !== 0" class="comment-info">
      <div class="info-header">
        <div class="header-title">用户评价</div>
        <div class="header-more">
          更多
          <i class="arrow-right"></i>
        </div>
      </div>
      <div class="info-user">
        <img :src="commentInfo.user.avatar" alt="">
        <span>{{commentInfo.user.uname}}</span>
      </div>
      <div class="info-detail">
        <p>{{commentInfo.content}}</p>
        <div class="info-other">
          <span class="date">{{commentInfo.created | showDate}}</span>
          <span>{{commentInfo.style}}</span>
        </div>
        <div class="info-imgs">
          <img :src="item" v-for="item in commentInfo.images">
        </div>
      </div>
    </div>
  </div>
</template>

<script>
  import {formatDate} from "@/common/utils";

  export default {
    name: "DetailCommentInfo",
    props: {
      commentInfo: {
        type: Object,
        default() {
          return {}
        }
      }
    },
    filters:{
      showDate(timeStamp) { //timeStamp后台传入的时间戳都是秒,JS的new Date()接收的是毫秒
        let date =new Date(timeStamp*1000)
        return formatDate(date, 'yyyy-MM-dd hh:mm:ss')
        /*
        时间戳转换成时间格式化字符串
        1、将时间戳转成Date对象
        2、将Date对象进行格式化，转成对应的字符串
        时间格式化字符串一般定义的格式是：'yyyy-MM-dd hh:mm:ss'
          y：year 年。可能传入的是 'yy-MM-dd' 那么希望的格式化时间字符串类似 18-10-20
          M: Month 月。之所以大写是为了和下面的m即minutes区分
          d: day 日
          h: hours 小时。如果是大写比如'yyyy-MM-dd HH:mm:ss',下午1点那就是13点,如果是小写的话就是1点
          m: minutes 分钟
        */
      }
    }
  }
</script>

